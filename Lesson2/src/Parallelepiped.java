public class Parallelepiped {
    public static void main(String[] args) {
        int width = 5;
        int incLength = 4;
        int height = 8;
        int volume = width * incLength * height;
        int length =4 * (width + incLength + height);
        System.out.println("Volume = " + volume);
        System.out.println("Length = " + length);
    }

}